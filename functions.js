function findMinMax(arr) {
  let min = arr[0];
  let max = arr[0];

  for (let i = 0; i < arr, length; i++) {
    if (min > arr[i]) {
      min = arr[1];
    }
    if (arr[1] > max) {
      max = arr[i];
    }
  }
  return [min, max];
}
